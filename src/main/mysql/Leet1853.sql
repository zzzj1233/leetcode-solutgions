#  Days table:
# +------------+
# | day        |
# +------------+
# | 2022-04-12 |
# | 2021-08-09 |
# | 2020-06-26 |
# +------------+
# 输出：
# +-------------------------+
# | day                     |
# +-------------------------+
# | Tuesday, April 12, 2022 |
# | Monday, August 9, 2021  |
# | Friday, June 26, 2020   |
# +-------------------------+
#

SELECT date_format('%w', `day`)
FROM Days